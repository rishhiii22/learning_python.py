# Airthmetic operators
a = 10
b = 5
c = a + b  # Addition
print(c)

# Assignment operators
a =  4-3 # Assign 4-3 in a
print(a)
b = 5
b += 2  # Increment b by 2
print(b)

# Comparison operators
a = 10
b = 20
print(a < b)  # Less than
print(a > b)  # Greater than
print(a == b)  # Equal to
print(a != b)  # Not equal to

# Logical operators
a = True
b = False
print(a and b)  # Logical AND (both must be True)
print(a or b)   # Logical OR (at least one must be True)
print(not a)    # Logical NOT (inverts the value)
print(not b)    # Logical NOT (inverts the value)

